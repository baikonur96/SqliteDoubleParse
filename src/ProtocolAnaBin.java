public class ProtocolAnaBin {
    private Integer sec;
    private Float val;

    public ProtocolAnaBin(Integer sec, Float val) {
        this.sec = sec;
        this.val = val;
    }

    public Integer getSec() {
        return sec;
    }

    public void setSec(Integer sec) {
        this.sec = sec;
    }

    public Float getVal() {
        return val;
    }

    public void setVal(Float val) {
        this.val = val;
    }
}
